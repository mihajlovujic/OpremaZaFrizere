package oprema.model;

public class Racun {

	private String broj;
	private String datumIzdavanja;
	private String datumPrometa;
	private String valutaPlacanja;
	
	
	
	public Racun()
	{
		
	}



	public String getBroj() {
		return broj;
	}



	public void setBroj(String broj) {
		this.broj = broj;
	}



	public String getDatumIzdavanja() {
		return datumIzdavanja;
	}



	public void setDatumIzdavanja(String datumIzdavanja) {
		this.datumIzdavanja = datumIzdavanja;
	}



	public String getDatumPrometa() {
		return datumPrometa;
	}



	public void setDatumPrometa(String datumPrometa) {
		this.datumPrometa = datumPrometa;
	}



	public String getValutaPlacanja() {
		return valutaPlacanja;
	}



	public void setValutaPlacanja(String valutaPlacanja) {
		this.valutaPlacanja = valutaPlacanja;
	}
	
	
	
}
